    public void addStudent(String std,String std_register)
    {
        String dob = null;
        String roll=txtRoll.getText();
        String name=txtFullName.getText();
        Date d=txtDOB.getDate();
        DateFormat df=new SimpleDateFormat("yyyy-MM-dd");
        dob=df.format(d);
        String address=txtAddress.getText();
        String email=txtEmail.getText();
        
        if(std.equals("--SELECT--") || name.equals("") || dob.equals("") || address.equals("") || roll.equals("") || email.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Enter all the fields");
        }
        else
        {
            try
            {
                Class.forName("com.mysql.jdbc.Driver");
                Connection con= DriverManager.getConnection("jdbc:mysql://localhost:3306/Attendance","root","");

                String sql="INSERT INTO "+std+"(RollNumber,Name,Class,DOB,Address,email)VALUES('"+roll+"','"+name+"','"+std+"','"+dob+"','"+address+"','"+email+"')";
                String sql1="INSERT INTO "+std_register+"(RollNumber,Name)VALUES('"+roll+"','"+name+"')";
                Statement st=con.createStatement();
                st.executeUpdate(sql);
                st.executeUpdate(sql1);
                JOptionPane.showMessageDialog(null, "Student Added Successfully");
                clear();

                st.close();
                con.close();
            }
            catch(Exception e)
            {
                System.out.println(e);
            }
        }

    }
    
