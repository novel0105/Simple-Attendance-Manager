    public void autoID(String std)
    {
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con= DriverManager.getConnection("jdbc:mysql://localhost:3306/Attendance","root","");
            
            String sql="SELECT Max(RollNumber) FROM "+std;
            Statement st= con.createStatement();
            
            ResultSet rs= st.executeQuery(sql);
            rs.next();
            
        
          
          if(rs.getString("Max(RollNumber)")==null)
          {
              txtRoll.setText("1");
          }
          else
          {
              int roll= Integer.parseInt(rs.getString("Max(RollNumber)"));
              roll++;
              txtRoll.setText(Integer.toString(roll));
              
          }
            
            
            rs.close();
            st.close();
            con.close();
     
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
    }
    
    public void viewStuds(String std)
    {
        try {
            Class.forName("com.mysql.jdbc.Driver");  
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/Attendance","root","");
            
            String sql="select * from "+std;
            PreparedStatement pst=con.prepareStatement(sql);
            ResultSet rs=pst.executeQuery();
            table.setModel(DbUtils.resultSetToTableModel(rs));
            
            rs.close();
            pst.close();
            con.close();
            
            
        } catch (Exception e) {
            System.out.println(e);
        }
    }
    
